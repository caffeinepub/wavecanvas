export interface ModMatrixSlot {
  source: number; // 0=LFO1, 1=LFO2, 2=LFO3, 3=ENV1, 4=ENV2, 5=ENV3, 6=Velocity, 7=ModWheel, 8=KeyTrack
  target: number; // 0=WTPos, 1=ScanSpeed, 2=Amplitude, 3=FilterCutoff, 4=FilterRes, 5=HarmonicEmphasis, 6=StereoSpread, 7=PhaseDistortion, 8=PitchFine, 9=SubLevel, 10=NoiseLevel
  amount: number;
  enabled: boolean;
}

export interface SynthPreset {
  name: string;
  mode: "wavetable" | "sc" | "experimental";
  wavetableType: string;
  // Oscillators
  wtPos: number;
  scanSpeed: number;
  unisonVoices: number;
  unisonDetune: number;
  stereoSpread: number;
  subLevel: number;
  subType: number; // 0=sine, 1=square
  noiseLevel: number;
  noiseType: number; // 0=white, 1=pink
  driftAmount: number;
  octave: number;
  fineTune: number;
  oscLevel: number;
  // Envelopes
  env1Attack: number;
  env1Decay: number;
  env1Sustain: number;
  env1Release: number;
  env2Attack: number;
  env2Decay: number;
  env2Sustain: number;
  env2Release: number;
  env3Attack: number;
  env3Decay: number;
  env3Sustain: number;
  env3Release: number;
  // Filter
  filter1Mode: number; // 0=LP, 1=HP, 2=BP, 3=Notch
  filter1Cutoff: number;
  filter1Res: number;
  filter1Drive: number;
  filter1EnvAmt: number;
  filter2Mode: number;
  filter2Cutoff: number;
  filter2Res: number;
  filter2Drive: number;
  filter2EnvAmt: number;
  filterRouting: number; // 0=serial, 1=parallel
  keyTracking: number;
  // LFOs
  lfo1Rate: number;
  lfo1Shape: number;
  lfo1Depth: number;
  lfo1Phase: number;
  lfo2Rate: number;
  lfo2Shape: number;
  lfo2Depth: number;
  lfo2Phase: number;
  lfo3Rate: number;
  lfo3Shape: number;
  lfo3Depth: number;
  lfo3Phase: number;
  // Effects
  chorusEnabled: number;
  chorusRate: number;
  chorusDepth: number;
  chorusSpread: number;
  delayEnabled: number;
  delayTime: number;
  delayFeedback: number;
  delaySpread: number;
  reverbEnabled: number;
  reverbSize: number;
  reverbDamping: number;
  reverbPreDelay: number;
  satEnabled: number;
  satDrive: number;
  limiterEnabled: number;
  limiterThreshold: number;
  limiterRelease: number;
  // Modulation Matrix
  modMatrix: ModMatrixSlot[];
  masterVolume: number;
}

const emptyMod: ModMatrixSlot[] = Array.from({ length: 8 }, () => ({
  source: 0,
  target: 0,
  amount: 0,
  enabled: false,
}));

function modSlot(
  source: number,
  target: number,
  amount: number,
): ModMatrixSlot {
  return { source, target, amount, enabled: true };
}

